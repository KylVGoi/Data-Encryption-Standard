def sbox(data48: int) -> int:
    """
    Applique les boîtes S du DES à un bloc de 48 bits pour le réduire à 32 bits.

    :param data48: Bloc de 48 bits après expansion.
    :return: Bloc de 32 bits après passage par les boîtes S.
    """
    global SBOX
    data = format(data48, '048b')
    data_into_32bytes = ""
    count_4_sbox = 0

    for i in range(0, 48, 6):
        data_treatment = data[i:i+6]

        two_bytes = int((data_treatment[0] + data_treatment[-1]),2)
        four_bytes = int(data_treatment[1:5], 2)

        sbox_id = SBOX[count_4_sbox][two_bytes][four_bytes]
        count_4_sbox += 1

        data_into_32bytes += format(sbox_id, '04b')

    return int(data_into_32bytes, 2)


def des_feistel_function(key: int, data: int) -> int:
    """
    Implémente la fonction Feistel du DES.

    :param key: Clé de sous-bloc de 48 bits.
    :param data: Bloc de 32 bits à transformer.
    :return: Bloc de 32 bits après la fonction Feistel.
    """
    global E_TABLE, P_TABLE
    # 1. Expansion E (32 bits -> 48 bits)
    expanded_data = permutation(data, E_TABLE, 32)

    # 2. XOR avec la sous-clé
    xored = expanded_data ^ key

    # 3. Passage par les S-Boxes (48 bits -> 32 bits)
    sboxed = sbox(xored)

    # 4. Permutation P sur la sortie S-Boxes
    final = permutation(sboxed, P_TABLE, 32)

    return final


